// round_constants[0] not used
static const uint32_t round_constants[11] = {
    0x00000000,
    0x01000000,
    0x02000000,
    0x04000000,
    0x08000000,
    0x10000000,
    0x20000000,
    0x40000000,
    0x80000000,
    0x1B000000,
    0x36000000
};

static uint8_t x_time(const uint8_t x)
{
    return (x << 1) ^ ((x & 0x80) ? 0x1B : 0x00);
}

static void set_s_box_value(uint8_t state[16])
{
    for (uint8_t i = 0; i < 16; i++)
    {
        state[i] = s_box[state[i]];
    }
}

static void shift_rows(uint8_t state[16])
{
    uint8_t temp = state[1];
    state[1]  = state[5];
    state[5]  = state[9];
    state[9]  = state[13];
    state[13] = temp;

    temp = state[2];
    state[2]  = state[10];
    state[10] = temp;
    temp = state[6];
    state[6]  = state[14];
    state[14] = temp;

    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7]  = state[3];
    state[3]  = temp;
}

static void mix_columns(uint8_t state[16])
{
    for (uint8_t i = 0; i < 4; i++)
    {
        const uint8_t col = i * 4;
        const uint8_t s0 = state[col];
        const uint8_t s1 = state[col + 1];
        const uint8_t s2 = state[col + 2];
        const uint8_t s3 = state[col + 3];

        const uint8_t t = s0 ^ s1 ^ s2 ^ s3;
        const uint8_t u = s0;

        state[col]     ^= t ^ x_time(s0 ^ s1);
        state[col + 1] ^= t ^ x_time(s1 ^ s2);
        state[col + 2] ^= t ^ x_time(s2 ^ s3);
        state[col + 3] ^= t ^ x_time(s3 ^ u);
    }
}

static void add_round_key(uint8_t state[16], const uint32_t round_key[kNumColumns])
{
    for (uint8_t  i = 0; i < kNumColumns; i++)
    {
        state[4 * i]     ^= (round_key[i] >> 24) & 0xFF;
        state[4 * i + 1] ^= (round_key[i] >> 16) & 0xFF;
        state[4 * i + 2] ^= (round_key[i] >> 8)  & 0xFF;
        state[4 * i + 3] ^= round_key[i] & 0xFF;
    }
}

static uint32_t sub_word(const uint32_t word)
{
    return ( (uint32_t)s_box[(word >> 24) & 0xFF] << 24 ) |
           ( (uint32_t)s_box[(word >> 16) & 0xFF] << 16 ) |
           ( (uint32_t)s_box[(word >> 8)  & 0xFF] << 8  ) |
           ( (uint32_t)s_box[word & 0xFF] );
}

// rotate word 1 byte to the left
static uint32_t rotate_word(const uint32_t word)
{
    return (word << 8) | (word >> 24);
}

void key_expansion(const uint8_t key[16], uint32_t w[kNumColumns * (kNumRounds + 1)])
{
    uint32_t temp = 0;

    for (uint8_t i = 0; i < kNumKeyWords; i++)
    {
        w[i] = ((uint32_t)key[4*i] << 24) |
               ((uint32_t)key[4*i + 1] << 16) |
               ((uint32_t)key[4*i + 2] << 8) |
               ((uint32_t)key[4*i + 3]);
    }


    for (int i = kNumKeyWords; i < kNumColumns * (kNumRounds + 1); i++)
    {
        temp = w[i - 1];
        if (i % kNumKeyWords == 0)
        {
            temp = sub_word(rotate_word(temp)) ^ round_constants[i / kNumKeyWords];
        }

        w[i] = w[i - kNumKeyWords] ^ temp;
    }
}

void aes_encrypt_block(uint8_t in[16], uint8_t out[16], const uint32_t round_keys[kNumColumns * (kNumRounds + 1)])
{
    uint8_t state[16] = { 0 };
    memcpy(state, in, sizeof(state));

    add_round_key(state, round_keys);

    // rounds 1 - 9: set_s_box_value, shift_rows, mix_columns, add_round_key
    for (uint8_t round = 1; round < kNumRounds; round++)
    {
        set_s_box_value(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, round_keys + round * kNumColumns);
    }

    // last round without mix_columns
    set_s_box_value(state);
    shift_rows(state);
    add_round_key(state, round_keys + kNumRounds * kNumColumns);

    memcpy(out, state, sizeof(state));
}
